menu = 'утиное филе;фланк-стейк;банановый пирог;плов'
print('Доступное меню: ', menu)
# формируем список используя ; как способ разделения значений
list_of_dishes = menu.split(';')
# выводим меню прелоразуя разделитель между юблюдами
print("На данный момент в меню есть: ", ', '.join(list_of_dishes))
