cheque = int(input('Введите стоимость первого товара: '))
cheque += int(input('Введите стоимость второго товара: '))
cheque += int(input('Введите стоимость третьего товара: '))
if cheque >= 1000:
  discount = cheque - (cheque * 10 / 100)
  print('Сумма покупки с учетом скидки 10% составляет: ', discount)
else:
  print('Ваш чек не входит в скидочную программу')
