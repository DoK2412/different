
number_1 = int(input('Введите двузначное число: '))
number_2 = int(input('Введите двузначное число: '))

if number_1 % 2 == 0  or number_2 % 2 == 0:
  print('Одно из чисел чётное. Учитель заслужил конфету!')
else: 
  if number_1 % 2 != 0  and number_2 % 2 != 0:
    print('Оба числа не четны. Учитель отдает конфету детям')