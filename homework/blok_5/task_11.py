
wages = int(input('Введите сумму заработка: '))

rate_1 = 50000
rate_2 = 10000

formula_30 = (30 * ((wages - rate_1) /100)) + (20 * ((rate_1 - rate_2)/100)) + (13 * (10000 / 100))
formula_15 = (20 * ((wages - rate_2)/100)) + (13 * (10000 / 100))

if wages > 50000:
  print('Ваша налоговая ставка 30% что составляет', formula_30)
elif (wages > 10000) and (wages < 50000):
  print('Ваша налоговая ставка 20% что составляет', formula_15)
else:
  print('Сумма не входит в диапозон')